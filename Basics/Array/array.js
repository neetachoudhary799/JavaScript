//array is collection of items stored at values and accessed by index
//array is mutable and can hold different data types
//array is object in javascript and has properties and methods

//creating array
let arr1 = [1, 2, 3, 4, 5];
let arr2 = ["apple", "banana", "orange"];
let arr3 = [1, "hello", true, null];

//key and value pairs
let arr4 = [
  { name: "Alice", age: 30 },
  { name: "Bob", age: 25 }
];

//accessing array elements
console.log(arr1[0]); // Output: 1
console.log(arr2[1]); // Output: "banana"
console.log(arr3[2]); // Output: true
console.log(arr4[0].name); // Output: "Alice"
console.log(arr4[1].age); // Output: 25

//array 
let array =[];
array.push(1);
array.push(2);
console.log(array); // Output: [1, 2]


