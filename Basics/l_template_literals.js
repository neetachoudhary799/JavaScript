//template literals
//Template literals are a modern way to work with strings using backticks ( ) instead of quotes.
//we can write one linecode it is easier to read and write multiline strings without needing escape characters for newlines or quotes.

let name = "Alice";
let age = 30;
let message = `Hello, my name is ${name} and I am ${age} years old.`;
console.log(message); // Output: "Hello, my name is Alice and I am 30 years old."